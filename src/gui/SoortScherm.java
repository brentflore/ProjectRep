package gui;

public enum SoortScherm 
{
    UNKOWN, LOGIN, KOPPEL, PLANNING;

	public String toString() 
        {
		switch (this) 
                {
		case LOGIN:
			return "LoginScherm";
		case KOPPEL:
			return "KoppelStudentPromotor";
                case PLANNING:
                        return "PlanningOverzichtScherm";
                case UNKOWN:
                        return "Selecteer een scherm";
		default:
			return "Soort scherm onbekend";
		}
	}
}