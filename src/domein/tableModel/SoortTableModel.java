package domein.tableModel;

public enum SoortTableModel 
{
	STUDENT, PROMOTOR, TEST;

	public String toString() 
        {
		switch (this) 
                {
		case STUDENT:
			return "Studenten";
		case PROMOTOR:
			return "Promotoren";
                case TEST:
                        return "testen";
		default:
			return "Soort tablemodel onbekend";
		}
	}
}
